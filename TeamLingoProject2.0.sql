CREATE TABLE ADMINISTRATORS (
IDADMINISTRATOR		NUMERIC (4) PRIMARY KEY,
ADM_NAME				VARCHAR (30) NOT NULL,
ADM_PASSWORD			VARCHAR (30) NOT NULL);

CREATE TABLE USERS (
IDUSER			NUMERIC(4) PRIMARY KEY,
USER_NAME		VARCHAR(30) NOT NULL,
USER_PASSWORD		VARCHAR(30) NOT NULL);

CREATE TABLE CHANGES (
SUGGESTED_CHANGE		VARCHAR(150),
IDUSER				NUMERIC(4) NOT NULL,
IDADMINISTRATOR		NUMERIC(4) NOT NULL,
CONSTRAINT USERS_IDUSER FOREIGN KEY (IDUSER) REFERENCES USERS(IDUSER),
CONSTRAINT ADMINISTRATORS_IDADMINISTRATOR FOREIGN KEY (IDADMINISTRATOR) REFERENCES ADMINISTRATORS(IDADMINISTRATOR));

CREATE TABLE LANGUAGES (
IDLANGUAGES		NUMERIC (3) NOT NULL,
_LANGUAGE    VARCHAR(20) UNIQUE,
LANGUAGE_FAMILY		VARCHAR(20) NOT NULL,
Progenative_Language	VARCHAR(20) NOT NULL,
CONSTRAINT language_and_id PRIMARY KEY (IDLANGUAGES, _LANGUAGE));

CREATE TABLE SYNTAX_RULES (
IDSYNTAX		NUMERIC (3) PRIMARY KEY,
WORD_ORDER		VARCHAR(4) NOT NULL,
DECLENSION_SYSTEM		VARCHAR(1) NOT NULL,
GENDER_RULES		VARCHAR(1) NOT NULL,
PARTICLE_WORDS		VARCHAR(1) NOT NULL,
_LANGUAGE			VARCHAR(20) NOT NULL,
CONSTRAINT SYNTAX_RULES_LANGUAGE FOREIGN KEY (_LANGUAGE) REFERENCES LANGUAGES(_LANGUAGE));

CREATE TABLE VERB_TENSE (
IDVERB_TENSE			NUMERIC(3) PRIMARY KEY,
_LANGUAGE				VARCHAR(30) NOT NULL,
PAST_SIMPLE				VARCHAR(1) NOT NULL,
PAST_PERFECT			VARCHAR(1) NOT NULL,
PAST_CONTINUOUS			VARCHAR(1) NOT NULL,
PRESENT_SIMPLE			VARCHAR(1) NOT NULL,
PRESENT_PERFECT			VARCHAR(1) NOT NULL,
PRESENT_CONTINUOUS		VARCHAR(1) NOT NULL,
FUTURE_SIMPLE			VARCHAR(1) NOT NULL,
FUTURE_PERFECT			VARCHAR(1) NOT NULL,
FUTURE_CONTINUOUS		VARCHAR(1) NOT NULL,
IDSYNTAX					NUMERIC(3) NOT NULL,
CONSTRAINT VERB_TENSE_IDSYNTAX		FOREIGN KEY (IDSYNTAX) REFERENCES SYNTAX_RULES (IDSYNTAX));

CREATE TABLE SENTENCES (
IDSENTENCES		NUMERIC(3) PRIMARY KEY,
_LANGUAGE		VARCHAR(30) NOT NULL,
SENTENCE_EX		VARCHAR(50),
IDSYNTAX		NUMERIC(3) NOT NULL,
CONSTRAINT SENTENCES_IDSYNTAX		FOREIGN KEY (IDSYNTAX) REFERENCES SYNTAX_RULES(IDSYNTAX));


CREATE TABLE MISCELLANEOUS (
WRITING_SYSTEM		VARCHAR(30) NOT NULL,
_LANGUAGE		VARCHAR(20) NOT NULL,
CONSTRAINT MISCELLANEOUS_RULES_LANGUAGE	FOREIGN KEY (_LANGUAGE) REFERENCES LANGUAGES (_LANGUAGE));

