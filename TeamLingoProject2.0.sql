CREATE DATABASE IF NOT EXISTS TeamLingo;
USE TeamLingo;

DROP TABLE IF EXISTS ADMINISTRATORS;
DROP TABLE IF EXISTS _USER;
DROP TABLE IF EXISTS CHANGES;
DROP TABLE IF EXISTS USERS_REQUEST_LANGUAGES;
DROP TABLE IF EXISTS LANGUAGES;
DROP TABLE IF EXISTS SYNTAX_RULES;
